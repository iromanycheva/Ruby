#capitalize - the first letter is capital, others are not
puts "hello".capitalize
puts "heLLo".capitalize
puts "hello World".capitalize

#upcase - all letters become uppercase
puts "iuliia".upcase
puts "blah blah blah".upcase

lowercase = "I'm patient"
uppercase = lowercase.upcase
puts uppercase

#downcase - all letters become downcase
uppercase = "BORIS#%32039"
puts uppercase.downcase

#swapcase - change the case every character
a = "iuLLia"
puts a.swapcase
